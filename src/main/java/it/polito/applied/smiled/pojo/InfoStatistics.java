package it.polito.applied.smiled.pojo;

public class InfoStatistics {

	private long numPost;
	private long numComment;
	private long numMetaComment;
	private long numFile;
	private long numLike;
	
	public long getNumPost() {
		return numPost;
	}
	public void setNumPost(long numPost) {
		this.numPost = numPost;
	}
	public long getNumComment() {
		return numComment;
	}
	public void setNumComment(long numComment) {
		this.numComment = numComment;
	}
	public long getNumMetaComment() {
		return numMetaComment;
	}
	public void setNumMetaComment(long numMetaComment) {
		this.numMetaComment = numMetaComment;
	}
	public long getNumFile() {
		return numFile;
	}
	public void setNumFile(long numFile) {
		this.numFile = numFile;
	}
	public long getNumLike() {
		return numLike;
	}
	public void setNumLike(long numLike) {
		this.numLike = numLike;
	}
	
	
	
}
